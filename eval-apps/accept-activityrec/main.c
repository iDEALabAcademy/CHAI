/* Deterministic trace data generated by seeded PRNG */
static threeAxis_t trace[SAMPLES_TO_COLLECT];
static unsigned traceIndex = 0;

/* Initialize deterministic trace data */
void init_trace(void) {
  unsigned long seed = 42;
  for (int i = 0; i < SAMPLES_TO_COLLECT; i++) {
    seed = seed * 1103515245 + 12345;
    /* Alternate between stationary-like and moving-like patterns */
    if (i < SAMPLES_TO_COLLECT / 2) {
      /* Stationary: small values centered around 0 */
      trace[i].x = (int8_t)((seed >> 16) % 11 - 5);
      seed = seed * 1103515245 + 12345;
      trace[i].y = (int8_t)((seed >> 16) % 11 - 5);
      seed = seed * 1103515245 + 12345;
      trace[i].z = (int8_t)((seed >> 16) % 11 - 5);
    } else {
      /* Moving: larger values */
      trace[i].x = (int8_t)((seed >> 16) % 81 - 40);
      seed = seed * 1103515245 + 12345;
      trace[i].y = (int8_t)((seed >> 16) % 81 - 40);
      seed = seed * 1103515245 + 12345;
      trace[i].z = (int8_t)((seed >> 16) % 81 - 40);
    }
    trace[i].padding = 0;
  }
}

void ACCEL_singleSample(threeAxis_t *taxis) {
  *taxis = trace[traceIndex++];
  if (traceIndex >= SAMPLES_TO_COLLECT) {
    traceIndex = 0;
  }
}

void getNextSample(void) {
  threeAxis_t threeAxis;
  ACCEL_singleSample(&threeAxis);
  aWin[currSamp][0] = (int32_t)threeAxis.x;
  aWin[currSamp][1] = (int32_t)threeAxis.y;
  aWin[currSamp][2] = (int32_t)threeAxis.z;
  currSamp++;

  if (currSamp >= ACCEL_WINDOW_SIZE) {
    currSamp = 0;
  }
}

void featurize(void) {
  /* Knob Variables Declaration Start */
  float loop_perf_ratio = 1.0;
  int magnitude_mode = 0;
  int sqrt_iterations = 2;
  /* Knob Variables Declaration End */

  mean[0] = mean[1] = mean[2] = 0;
  stddev[0] = stddev[1] = stddev[2] = 0;
  uint16_t i;

  /* APPROXIMATION 1: Loop Perforation - Truncate iterations for mean calculation */
  uint16_t max_samples = (uint16_t)(ACCEL_WINDOW_SIZE * loop_perf_ratio);
  for (i = 0; i < max_samples; i++) {
    mean[0] += aWin[i][0];  /* x */
    mean[1] += aWin[i][1];  /* y */
    mean[2] += aWin[i][2];  /* z */
  }
  mean[0] >>= 2;
  mean[1] >>= 2;
  mean[2] >>= 2;

  /* APPROXIMATION 1: Loop Perforation - Truncate iterations for deviation calculation */
  for (i = 0; i < max_samples; i++) {
    stddev[0] += aWin[i][0] > mean[0] ? aWin[i][0] - mean[0]
                                       : mean[0] - aWin[i][0];
    stddev[1] += aWin[i][1] > mean[1] ? aWin[i][1] - mean[1]
                                       : mean[1] - aWin[i][1];
    stddev[2] += aWin[i][2] > mean[2] ? aWin[i][2] - mean[2]
                                       : mean[2] - aWin[i][2];
  }
  stddev[0] >>= 2;
  stddev[1] >>= 2;
  stddev[2] >>= 2;

  /* APPROXIMATION 2: Surrogate Functions - Replace expensive sqrtf() operations */
  if (magnitude_mode == 0) {
    /* Original exact computation using floating-point sqrt */
    float meanmag_f = (float)
      ((mean[0]*mean[0]) + (mean[1]*mean[1]) + (mean[2]*mean[2]));
    float stddevmag_f = (float)
      ((stddev[0]*stddev[0]) + (stddev[1]*stddev[1]) + (stddev[2]*stddev[2]));

    meanmag_f   = sqrtf(meanmag_f);
    stddevmag_f = sqrtf(stddevmag_f);

    meanmag   = (long)meanmag_f;
    stddevmag = (long)stddevmag_f;
  } else if (magnitude_mode == 1) {
    /* Manhattan distance approximation: |x| + |y| + |z| */
    meanmag = (mean[0] < 0 ? -mean[0] : mean[0]) + 
              (mean[1] < 0 ? -mean[1] : mean[1]) + 
              (mean[2] < 0 ? -mean[2] : mean[2]);
    stddevmag = (stddev[0] < 0 ? -stddev[0] : stddev[0]) + 
                (stddev[1] < 0 ? -stddev[1] : stddev[1]) + 
                (stddev[2] < 0 ? -stddev[2] : stddev[2]);
  } else if (magnitude_mode == 2) {
    /* Chebyshev distance approximation: max(|x|, |y|, |z|) */
    long abs_mean[3] = {mean[0] < 0 ? -mean[0] : mean[0],
                        mean[1] < 0 ? -mean[1] : mean[1],
                        mean[2] < 0 ? -mean[2] : mean[2]};
    long abs_stddev[3] = {stddev[0] < 0 ? -stddev[0] : stddev[0],
                          stddev[1] < 0 ? -stddev[1] : stddev[1],
                          stddev[2] < 0 ? -stddev[2] : stddev[2]};
    
    meanmag = abs_mean[0];
    if (abs_mean[1] > meanmag) meanmag = abs_mean[1];
    if (abs_mean[2] > meanmag) meanmag = abs_mean[2];
    
    stddevmag = abs_stddev[0];
    if (abs_stddev[1] > stddevmag) stddevmag = abs_stddev[1];
    if (abs_stddev[2] > stddevmag) stddevmag = abs_stddev[2];
  } else if (magnitude_mode == 3) {
    /* Division-free integer sqrt using digit-by-digit method (shifts + subtracts only) */
    long mean_squared = (mean[0]*mean[0]) + (mean[1]*mean[1]) + (mean[2]*mean[2]);
    long stddev_squared = (stddev[0]*stddev[0]) + (stddev[1]*stddev[1]) + (stddev[2]*stddev[2]);
    
    /* Integer sqrt for meanmag — no division, no floating point */
    {
      long n = mean_squared;
      long x = 0;
      long bit = 1L << 30;
      while (bit > n) bit >>= 2;
      while (bit != 0) {
        if (n >= x + bit) {
          n -= x + bit;
          x = (x >> 1) + bit;
        } else {
          x >>= 1;
        }
        bit >>= 2;
      }
      meanmag = x;
    }
    
    /* Integer sqrt for stddevmag — no division, no floating point */
    {
      long n = stddev_squared;
      long x = 0;
      long bit = 1L << 30;
      while (bit > n) bit >>= 2;
      while (bit != 0) {
        if (n >= x + bit) {
          n -= x + bit;
          x = (x >> 1) + bit;
        } else {
          x >>= 1;
        }
        bit >>= 2;
      }
      stddevmag = x;
    }
  } else if (magnitude_mode == 4) {
    /* Skip sqrt entirely — use sum-of-squares directly (pure integer, no float) */
    meanmag = (mean[0]*mean[0]) + (mean[1]*mean[1]) + (mean[2]*mean[2]);
    stddevmag = (stddev[0]*stddev[0]) + (stddev[1]*stddev[1]) + (stddev[2]*stddev[2]);
  }
}

int classify(void) {
  /* Knob Variables Declaration Start */
  int early_exit_enabled = 0;
  int confidence_threshold = 8;
  int min_comparisons = 2;
  int perforation_factor = 100;
  /* Knob Variables Declaration End */
  
  int move_less_error = 0;
  int stat_less_error = 0;
  int i;
  
  // Calculate truncated loop limit based on perforation factor
  int truncated_limit = (MODEL_COMPARISONS * perforation_factor) / 100;
  if (truncated_limit < NUM_FEATURES) {
    truncated_limit = NUM_FEATURES; // Ensure at least one comparison
  }

  /* Classify the current sample based on relative similarity to
   * the first MODEL_COMPARISONS entries of the moving and stationary models. */
  for (i = 0; i < truncated_limit; i += NUM_FEATURES) {
    long int stat_mean_err = (stationary[i] > meanmag)
                              ? (stationary[i] - meanmag)
                              : (meanmag - stationary[i]);

    long int stat_sd_err = (stationary[i + 1] > stddevmag)
                            ? (stationary[i + 1] - stddevmag)
                            : (stddevmag - stationary[i + 1]);

    long int move_mean_err = (moving[i] > meanmag)
                              ? (moving[i] - meanmag)
                              : (meanmag - moving[i]);

    long int move_sd_err = (moving[i + 1] > stddevmag)
                            ? (moving[i + 1] - stddevmag)
                            : (stddevmag - moving[i + 1]);

    if (move_mean_err < stat_mean_err) {
      move_less_error++;
    } else {
      stat_less_error++;
    }

    if (move_sd_err < stat_sd_err) {
      move_less_error++;
    } else {
      stat_less_error++;
    }
    
    // Early-exit check: after minimum comparisons, check confidence
    if (early_exit_enabled && i >= (min_comparisons * NUM_FEATURES)) {
      int vote_difference = (move_less_error > stat_less_error) 
                            ? (move_less_error - stat_less_error)
                            : (stat_less_error - move_less_error);
      if (vote_difference >= confidence_threshold) {
        // High confidence reached - exit early
        break;
      }
    }
  }

  if (move_less_error > stat_less_error) {
    return 1;
  } else {
    return 0;
  }
}

void initializeNVData(void) {
  __NV_movingCount = 0;
  __NV_stationaryCount = 0;
  __NV_totalCount = 0;
}

int main(void) {
  initializeNVData();
  init_trace();

  /* Temporal Decimation: only featurize+classify every decimation_step samples */
  int decimation_step = 1;  /* 1=baseline, 2=every other, 3=every 3rd... */
  int last_class = 0;
  int sample_counter = 0;

  while (1) {
    if (__NV_totalCount > SAMPLES_TO_COLLECT) {
      break;
    }
    getNextSample();

    if (sample_counter % decimation_step == 0) {
      featurize();
      last_class = classify();
    }
    sample_counter++;

    int class = last_class;

    __NV_totalCount++;

    if (class) {
      (__NV_movingCount)++;
    } else {
      (__NV_stationaryCount)++;
    }
  }

#ifdef LOCAL_RUN
  printf("%d,%d\n", __NV_movingCount, __NV_stationaryCount);
#endif

#ifndef LOCAL_RUN
  indicate_end();
#endif

  return 0;
}
