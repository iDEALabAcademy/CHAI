/* Deterministic trace data generated by seeded PRNG */
static threeAxis_t trace[SAMPLES_TO_COLLECT];
static unsigned traceIndex = 0;

/* Initialize deterministic trace data */
void init_trace(void) {
  unsigned long seed = 42;
  for (int i = 0; i < SAMPLES_TO_COLLECT; i++) {
    seed = seed * 1103515245 + 12345;
    /* Alternate between stationary-like and moving-like patterns */
    if (i < SAMPLES_TO_COLLECT / 2) {
      /* Stationary: small values centered around 0 */
      trace[i].x = (int8_t)((seed >> 16) % 11 - 5);
      seed = seed * 1103515245 + 12345;
      trace[i].y = (int8_t)((seed >> 16) % 11 - 5);
      seed = seed * 1103515245 + 12345;
      trace[i].z = (int8_t)((seed >> 16) % 11 - 5);
    } else {
      /* Moving: larger values */
      trace[i].x = (int8_t)((seed >> 16) % 81 - 40);
      seed = seed * 1103515245 + 12345;
      trace[i].y = (int8_t)((seed >> 16) % 81 - 40);
      seed = seed * 1103515245 + 12345;
      trace[i].z = (int8_t)((seed >> 16) % 81 - 40);
    }
    trace[i].padding = 0;
  }
}

void ACCEL_singleSample(threeAxis_t *taxis) {
  *taxis = trace[traceIndex++];
  if (traceIndex >= SAMPLES_TO_COLLECT) {
    traceIndex = 0;
  }
}

void getNextSample(void) {
  threeAxis_t threeAxis;
  ACCEL_singleSample(&threeAxis);
  aWin[currSamp][0] = (int32_t)threeAxis.x;
  aWin[currSamp][1] = (int32_t)threeAxis.y;
  aWin[currSamp][2] = (int32_t)threeAxis.z;
  currSamp++;

  if (currSamp >= ACCEL_WINDOW_SIZE) {
    currSamp = 0;
  }
}

void featurize(void) {
  mean[0] = mean[1] = mean[2] = 0;
  stddev[0] = stddev[1] = stddev[2] = 0;
  uint16_t i;

  /* Compute the average accel value in the window.
   * Use right shift by 2 instead of division by 4 (ACCEPT style). */
  for (i = 0; i < ACCEL_WINDOW_SIZE; i++) {
    mean[0] += aWin[i][0];  /* x */
    mean[1] += aWin[i][1];  /* y */
    mean[2] += aWin[i][2];  /* z */
  }
  mean[0] >>= 2;
  mean[1] >>= 2;
  mean[2] >>= 2;

  /* Compute the (nonstandard) deviation: mean absolute deviation. */
  for (i = 0; i < ACCEL_WINDOW_SIZE; i++) {
    stddev[0] += aWin[i][0] > mean[0] ? aWin[i][0] - mean[0]
                                       : mean[0] - aWin[i][0];
    stddev[1] += aWin[i][1] > mean[1] ? aWin[i][1] - mean[1]
                                       : mean[1] - aWin[i][1];
    stddev[2] += aWin[i][2] > mean[2] ? aWin[i][2] - mean[2]
                                       : mean[2] - aWin[i][2];
  }
  stddev[0] >>= 2;
  stddev[1] >>= 2;
  stddev[2] >>= 2;

  /* Compute the magnitude of each feature vector (uses float sqrt). */
  float meanmag_f = (float)
    ((mean[0]*mean[0]) + (mean[1]*mean[1]) + (mean[2]*mean[2]));
  float stddevmag_f = (float)
    ((stddev[0]*stddev[0]) + (stddev[1]*stddev[1]) + (stddev[2]*stddev[2]));

  meanmag_f   = sqrtf(meanmag_f);
  stddevmag_f = sqrtf(stddevmag_f);

  meanmag   = (long)meanmag_f;
  stddevmag = (long)stddevmag_f;
}

int classify(void) {
  int move_less_error = 0;
  int stat_less_error = 0;
  int i;

  /* Classify the current sample based on relative similarity to
   * the first MODEL_COMPARISONS entries of the moving and stationary models. */
  for (i = 0; i < MODEL_COMPARISONS; i += NUM_FEATURES) {
    long int stat_mean_err = (stationary[i] > meanmag)
                              ? (stationary[i] - meanmag)
                              : (meanmag - stationary[i]);

    long int stat_sd_err = (stationary[i + 1] > stddevmag)
                            ? (stationary[i + 1] - stddevmag)
                            : (stddevmag - stationary[i + 1]);

    long int move_mean_err = (moving[i] > meanmag)
                              ? (moving[i] - meanmag)
                              : (meanmag - moving[i]);

    long int move_sd_err = (moving[i + 1] > stddevmag)
                            ? (moving[i + 1] - stddevmag)
                            : (stddevmag - moving[i + 1]);

    if (move_mean_err < stat_mean_err) {
      move_less_error++;
    } else {
      stat_less_error++;
    }

    if (move_sd_err < stat_sd_err) {
      move_less_error++;
    } else {
      stat_less_error++;
    }
  }

  if (move_less_error > stat_less_error) {
    return 1;
  } else {
    return 0;
  }
}

void initializeNVData(void) {
  __NV_movingCount = 0;
  __NV_stationaryCount = 0;
  __NV_totalCount = 0;
}

int main(void) {
  initializeNVData();
  init_trace();

  while (1) {
    if (__NV_totalCount > SAMPLES_TO_COLLECT) {
      break;
    }
    getNextSample();
    featurize();

    int class = classify();

    __NV_totalCount++;

    if (class) {
      __NV_movingPct =
        ((float)__NV_movingCount / (float)__NV_totalCount) * 100.0f;
      (__NV_movingCount)++;
    } else {
      __NV_stationaryPct =
        ((float)__NV_stationaryCount / (float)__NV_totalCount) * 100.0f;
      (__NV_stationaryCount)++;
    }
  }

#ifdef LOCAL_RUN
  printf("%d,%d\n", __NV_movingCount, __NV_stationaryCount);
#endif

#ifndef LOCAL_RUN
  indicate_end();
#endif

  return 0;
}
