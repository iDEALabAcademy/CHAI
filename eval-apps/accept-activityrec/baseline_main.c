/* ------ Parameters ------ */
#define MODEL_SIZE 190
#define MODEL_SIZE_PLUS_WARMUP (MODEL_SIZE + 10)
#define ACCEL_WINDOW_SIZE 4
#define SAMPLES_TO_COLLECT 512
#define NUM_FEATURES 2
#define MODEL_COMPARISONS 20

/* ------ Types ------ */
typedef struct {
  int8_t x;
  int8_t y;
  int8_t z;
  uint8_t padding;
} threeAxis_t;

/* ------ Globals ------ */
